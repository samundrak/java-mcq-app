    void seedForm() {
        try {
            ResultSet rs = new Subject().getSubjectById(this.subjectId);
            while (rs.next()) {
                subjectInput.setText(rs.getString("name"));
                saveBtn.setText("Update");
            }

        } catch (SQLException ex) {
            Logger.getLogger(SubjectForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
