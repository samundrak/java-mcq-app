    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            String subject = subjectInput.getText();
            if (subject.isEmpty()) {
                return;
            }
            // TODO add your handling code here:
            Subject subjectService = new Subject();
            if (this.subjectId > -1) {
                subjectService.updateSubjectById(this.subjectId, subject);
            } else {
                subjectService.addSubject(subject);
            }

            subjectInput.setText("");
            JOptionPane.showMessageDialog(this, "Subject successfully" + (this.subjectId > -1 ? " Updated" : " Added"));
            this.dispose();
            this.hide();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

